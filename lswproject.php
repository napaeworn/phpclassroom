          <h4>ภูมิประเทศ </h4><br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;จังหวัดเชียงใหม่มีพื้นที่ 20,107.057 ตารางกิโลเมตรหรือประมาณ 12,566,911 ไร่ มีพื้นที่กว้างใหญ่เป็นอันดับที่ 1 ของภาคเหนือ และเป็นอันดับ 2 ของประเทศ รองจากจังหวัดนครราชสีมา ลักษณะภูมิประเทศโดยทั่วไปมีสภาพพื้นที่เป็นภูเขาและป่าละเมาะ มีที่ราบอยู่ตอนกลางตามสองฟากฝั่งแม่น้ำปิง มีภูเขาที่สูงที่สุดในประเทศไทยคือ ดอยอินทนนท์ สูงประมาณ 2,565 เมตร อยู่ในเขตอำเภอจอมทอง นอกจากนี้ยังมีดอยอื่นที่มีความสูงรองลงมาอีกหลายแห่ง เช่น ดอยผ้าห่มปก (อำเภอฝาง) สูง 2,285 เมตร ดอยหลวงเชียงดาว (อำเภอเชียงดาว) สูง 2,170 เมตร ดอยสุเทพ (อำเภอเมืองเชียงใหม่) สูง 1,601 เมตร สภาพพื้นที่แบ่งออกได้เป็น 2 ลักษณะคือ<br><br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;1.	พื้นที่ภูเขา คิดเป็นพื้นที่ประมาณร้อยละ 80 ของจังหวัด ประกอบด้วยทิวเขาอินทนนท์ (หรือถนนธงชัยตะวันออก) ตั้งอยู่ทางทิศตะวันตกของจังหวัด พาดยาวจากทิศเหนือจรดใต้ ตามแนวรอยต่อกับจังหวัดแม่ฮ่องสอน และทิวเขาขุนตาน ซึ่งตั้งอยู่ทางทิศตะวันออกของจังหวัด พาดผ่านในทิศเหนือ-ใต้  พื้นที่ภูเขาส่วนใหญ่เป็นป่าต้นน้ำลำธาร ไม่เหมาะสมต่อการเพาะปลูก ส่วนบางพื้นที่เป็นพื้นที่อยู่อาศัยของชาวเขาชาติพันธุ์ต่าง ๆ<br><br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2.	พื้นที่ราบลุ่มน้ำและที่ราบเชิงเขา กระจายอยู่ทั่วไประหว่างหุบเขาทอดตัวในแนวเหนือ-ใต้ ได้แก่ ที่ราบลุ่มน้ำปิง ลุ่มน้ำฝาง ลุ่มน้ำแม่งัด เป็นพื้นที่ที่มีความอุดมสมบูรณ์เหมาะสมต่อการเกษตร<br><br>

