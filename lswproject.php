          <h3>จังหวัดเชียงใหม่</h3><br>
  
          &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;เชียงใหม่ (คำเมือง:เจียงใหม่) เป็นจังหวัดหนึ่งทางภาคเหนือของประเทศไทย ครอบคลุมพื้นที่ประมาณ 20,107 ตารางกิโลเมตรซึ่งใหญ่เป็นอันดับ 2 ของประเทศ มีประชากรราว 1.76 ล้านคน มากเป็นอันดับ 5 ของประเทศ เชียงใหม่มีอำเภอเมืองเชียงใหม่เป็นศูนย์กลางของจังหวัดและแบ่งการปกครองออกเป็น 25 อำเภอ คือ
          เมื่อ พ.ศ. 2552 มีการจัดตั้งอำเภอกัลยาณิวัฒนาเป็นอำเภอลำดับที่ 25 ของจังหวัด และลำดับที่ 878 ของประเทศ ซึ่งเป็นอำเภอล่าสุดของไทย จังหวัดเชียงใหม่มีประวัติศาสตร์อันยาวนาน เคยเป็นเมืองหลวงของอาณาจักรล้านนาแต่โบราณ มี "คำเมือง" เป็นภาษาท้องถิ่น มีเอกลักษณ์เฉพาะตัวทั้งด้านประเพณีวัฒนธรรม และมีแหล่งท่องเที่ยวจำนวนมาก โดยเริ่มวางตัวเป็นนครสร้างสรรค์ และได้รับการประกาศเป็นเมืองสร้างสรรค์ของโลกทางด้านหัตถกรรมและศิลปะพื้นบ้านเมื่อปี พ.ศ. 2560 ปัจจุบันกำลังพิจารณาสมัครเมืองมรดกโลกจากองค์การยูเนสโก เชียงใหม่ยังถือเป็นศูนย์กลางด้านดาราศาสตร์ของเอเชียตะวันออกเฉียงใต้ โดยเป็นที่ตั้งของหอดูดาวแห่งชาติและอุทยานดาราศาสตร์แห่งชาติ<br><br>
          <h4>คำขวัญ</h4> &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<b>"ดอยสุเทพเป็นศรี ประเพณีเป็นสง่า บุปผาชาติล้วนงามตา นามล้ำค่านครพิงค์"</b><br><br>
          <h4>ที่ตั้ง</h4><br>
          &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;จังหวัดเชียงใหม่ (ตัวอำเภอเมือง) ตั้งอยู่ ณ ละติจูด 18 องศาเหนือ ลองติจูด 98 องศาตะวันออก สูงจากระดับน้ำทะเลประมาณ 310 เมตร ส่วนกว้างจากทิศตะวันตกจรดทิศตะวันออกประมาณ 138 กิโลเมตร ส่วนยาวจากทิศเหนือจรดทิศใต้ประมาณ 428 กิโลเมตร ห่างจากกรุงเทพมหานคร 696 กิโลเมตร<br><br>
